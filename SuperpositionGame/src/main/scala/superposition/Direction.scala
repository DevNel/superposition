package superposition

/**
 * The cardinal directions.
 */
private object Direction extends Enumeration {
  /**
   * The up direction.
   */
  val Up: Value = Value

  /**
   * The down direction.
   */
  val Down: Value = Value

  /**
   * The left direction.
   */
  val Left: Value = Value

  /**
   * The right direction.
   */
  val Right: Value = Value
}
